#include <stdio.h>
#include <math.h>

int main() {
    double G, F, Y, x, a;
    scanf("%lf", &x);
    scanf("%lf", &x);
    G = (7 * (54 * pow(a, 2) - 33 * a * x + 4 * pow(x, 2))) / (-4 * pow(a, 2) + a * x + 5 * pow(x, 2));
    F = sin(3 * (-10 * pow(a, 2) + 3 * a * x + 7 * pow(x, 2)));
    Y = asin(5 * pow(a, 2) - 9 * a * x + 4 * pow(x, 2));
    printf("%.3f\n", G);
    printf("%.3f\n", F);
    printf("%.3f\n", Y);
    return 0;
}